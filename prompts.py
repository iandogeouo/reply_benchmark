def completeness(petition, reply):
    return f"""你是一個公文回覆品質評審。

以下是一封市民陳情內容，以及公務員的建議回覆。
請評估「建議回覆」是否完整回應了陳情中的核心問題。

【市民陳情】
{petition}

【建議回覆】
{reply}

請：
1. 找出陳情中的核心問題（1-3個）
2. 評估建議回覆有沒有回答到這些問題
3. 給出 1-5 分（1=完全沒回答, 5=完整回答）

只回傳 JSON，不要有其他文字：
{{"score": 分數, "core_issues": ["問題1", "問題2"], "missing": ["未回答的點，如果都有回答則為空陣列"], "reason": "一句話說明扣分原因，如果滿分則說明原因"}}"""


def fidelity(civil, reply):
    return f"""你是一個公文回覆品質評審。

以下是公務員想表達的核心內容，以及 AI 生成的建議回覆。
請評估「建議回覆」是否忠實呈現了公務員提供的資訊，沒有超出、扭曲或遺漏重要內容。

【公務員想回答的內容】
{civil}

【AI 建議回覆】
{reply}

評估重點：
- 有沒有新增公務員沒有提到的承諾或資訊
- 有沒有扭曲或改變公務員的意思
- 有沒有遺漏公務員想表達的重要資訊
- 有沒有過度美化或誇大公務員的立場

注意：以下情況不算「額外加的內容」：
- 回覆中的機關名稱、局長署名（這些屬於固定格式）
- 引用或摘要市民陳情的內容
- 標準結語（如「謝謝您來信」、「敬祝順心如意」）
- 服務態度用語（如「歡迎隨時聯繫」）

只有涉及具體承諾、時程、金額、保證結果的新增內容才需要標記。


請給出 1-5 分（1=嚴重扭曲或超出, 5=完全忠實呈現）

只回傳 JSON，不要有其他文字：
{{"score": 分數, "added": ["AI 額外加的承諾或資訊，沒有則為空陣列"], "missing": ["遺漏的重要資訊，沒有則為空陣列"], "distorted": ["被扭曲的內容，沒有則為空陣列"], "reason": "一句話總結"}}"""


